export interface Festival {
  id: string;
  name: string;
  localNames: Record<string, string>;
  startDate: string; // ISO date
  endDate: string; // ISO date
  type: 'national' | 'religious' | 'regional' | 'cultural';
  regions: string[];
  theme: FestivalTheme;
  animations: FestivalAnimations;
  greetings: Record<string, string>;
  specialOffers?: SpecialOffer[];
}

export interface FestivalTheme {
  primary: string;
  secondary: string;
  accent: string;
  background: string;
  backgroundGradient: string;
  textPrimary: string;
  textSecondary: string;
  decorativeElements: string[];
  iconSet: string;
  fontFamily?: string;
}

export interface FestivalAnimations {
  particles: ParticleConfig[];
  transitions: string;
  backgroundAnimation?: string;
  iconAnimations?: Record<string, string>;
}

export interface ParticleConfig {
  type: 'diya' | 'flower' | 'rangoli' | 'firework' | 'star' | 'custom';
  count: number;
  size: { min: number; max: number };
  speed: { min: number; max: number };
  colors: string[];
  customImage?: string;
}

export interface SpecialOffer {
  type: 'fee_discount' | 'bonus_amount' | 'special_quote';
  value: number | string;
  message: Record<string, string>;
}

